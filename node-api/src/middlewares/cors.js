
// Function that sets the CORS (Cross-Origin Resource Sharing) headers on HTTP responses

export const corsMdw = (req, res, next) => {
  res.header("Access-Control-Allow-Origin", "*");
  res.header(
    "Access-Control-Allow-Headers",
    "Origin, X-Requested-With, Content-Type, Accept, Authorization"
  );
  next();
};